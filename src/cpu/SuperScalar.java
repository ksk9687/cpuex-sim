		protected int fmul2(int a, int b) {
			int rs, re, rm;
			
			int as = 1 & (a >>> 31);
			int ae = 0xff & (a >>> 23);
			int am = 0x7fffff & a;
			
			int bs = 1 & (b >>> 31);
			int be = 0xff & (b >>> 23);
			int bm = 0x7fffff & b;
			
			long amm = (1 << 23) | am;
			long bmm = (1 << 23) | bm;
			
			long ah = amm >>> 12, al = amm & ((1 << 12) - 1);
			long bh = bmm >>> 12, bl = bmm & ((1 << 12) - 1);
			
			long oh = ah * bh;
			long om1 = (ah * bl) >>> 11;
			long om2 = (al * bh) >>> 11;
			
			long rmm = (oh << 1) + om1 + om2 + 2;
			
			long ree = (ae - 127) + (be - 127) + 127;
			
			while ((rmm >>> 24) != 0) {
				rmm >>>= 1;
				ree++;
			}
			
			rs = as ^ bs;
			re = (int)ree;
			rm = (int)(rmm & ((1 << 23) - 1));
			int r = ((1 & rs) << 31) | ((0xff & re) << 23) | (0x7fffff & rm);
			return r;
		}
		
		@Override
		protected int finv(int a) {
			int rs, re, rm;
			
			int as = 1 & (a >>> 31);
			int ae = 0xff & (a >>> 23);
			int am = 0x7fffff & a;
			
			long amm = (1 << 23) | am;
			long x1 = amm >>> 12;
			long x2 = amm & ((1 << 12) - 1);
			
			long c = finv_table[(int)(x1 - (1 << 11))];
			long x = (x1 << 12) | (x2 ^ ((1 << 12) - 1));
			
			long ch = c >>> 12, cl = c & ((1 << 12) - 1);
			long xh = x >>> 12, xl = x & ((1 << 12) - 1);
			
			long oh = ch * xh;
			long omm1 = (ch * xl) >>> 11;
			long omm2 = (cl * xh) >>> 11;
			
			long om1 = (oh << 1) + omm1 + omm2 + 2;
			long om2 = (om1 & (1L << 24)) != 0 ? (om1 >>> 1) : om1;
			long oe = 254 - ae - ((om1 & (1L << 24)) != 0 ? 0 : 1);
			
			// if (oe <= 0 || 255 <= oe) return false;
			
			rm = (int) (om2 & ((1 << 23) - 1));
			re = (int) oe;
			rs = as;
			int r = ((1 & rs) << 31) | ((0xff & re) << 23) | (0x7fffff & rm);
			return r;
		}
		
//		@Override
		protected int fsqrt2(int a) {
			if (itof(a) < 0) return ftoi(Float.NaN);
			
			int rs, re, rm;
			
			int as = 1 & (a >>> 31);
			int ae = 0xff & (a >>> 23);
			int am = 0x7fffff & a;
			
			long amm = (1 << 23) | am;
			long oe = 63 + (ae >>> 1);
			
			int idx = (((ae & 1) ^ 1) << 10) | ((am >>> 13) ^ (1 << 10));
			long c = fsqrt_table[idx];
			
			long x1 = amm >>> 13;
			long x2 = amm & ((1 << 13) - 1);
			
			long x = 0;
			x |= x1 << 13;
			x |= (1 + ((x2 >>> 12) & 1)) << 11;
			x |= (x2 & ((1 << 12) - 1)) >>> 1;
			
			if (a == 0) {
				x = c = 0;
				oe = 0;
			}
			
			long ch = c >>> 12, cl = c & ((1 << 12) - 1);
			long xh = x >>> 12, xl = x & ((1 << 12) - 1);
			
			long oh = ch * xh;
			long omm1 = (ch * xl) >>> 11;
			long omm2 = (cl * xh) >>> 11;
			
			long om1 = (oh << 1) + omm1 + omm2 + 2;
			long om2 = (om1 & (1L << 24)) != 0 ? (om1 >>> 1) : om1;
			long oe2 = oe + ((om1 & (1L << 24)) != 0 ? 1 : 0);
			
			rm = (int) (om2 & ((1 << 23) - 1));
			re = (int) oe2;
			rs = as; // !?
			int r = ((1 & rs) << 31) | ((0xff & re) << 23) | (0x7fffff & rm);
			return r;
		}
		
